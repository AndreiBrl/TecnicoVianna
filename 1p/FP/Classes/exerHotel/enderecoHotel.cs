class Endereco{
    private string logradouro;
    private string rua;

    private string cidade;

    public string Logradouro{
        get{return logradouro;}
        set{logradouro=value;}
    }
    
    public string Rua{
        get{return rua;}
        set{rua=value;}
    }
        
    public string Cidade{
        get{return cidade;}
        set{cidade=value;}
}
}